public class Person {
    private int age;
    private String name;
    private String job;
    private String lastname;
    private String firstname;


    public Person(int age, String name,  String job, String lastname, String firstname) {
        this.age = age;
        this.name = name;
        this.job = job;
        this.lastname = lastname;
        this.firstname = firstname;
    }


}
